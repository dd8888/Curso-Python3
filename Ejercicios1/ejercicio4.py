# Dados dos números mostrar operaciones matemáticas

numero1 = float(input("Introduzca el primer numero: "))
numero2 = float(input("Introduzca el segundo numero: "))

print("Suma =", numero1 + numero2, "Resta =", numero1 - numero2, "Multiplicación =", numero1 * numero2, "División =", numero1 / numero2)